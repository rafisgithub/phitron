#include<bits/stdc++.h> // Includes all standard libraries
using namespace std;

int main() {

    cout << "Hello World\n"; // O(1) - Constant time operation

    int n;        // O(1) - Variable declaration is a constant-time operation
    cin >> n;     // O(1) - Reading a single integer from input

    cout << (n*(n+1))/2; // O(1) - Arithmetic operation and output

    return 0;
}
